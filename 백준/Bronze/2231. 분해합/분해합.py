# 💡 보너스 팁 (범위 줄이기)
# 1부터 N까지 다 돌면 N이 1,000,000일 때 백만 번 연산합니다. 파이썬은 충분히 빠르지만, 더 똑똑하게 범위를 줄일 수도 있습니다.

# 생성자와 N의 차이는 **최대 (자릿수 × 9)**만큼만 납니다. (각 자릿수가 가질 수 있는 최대값이 9니까요!) 즉, N - (자릿수 * 9)부터만 검사해도 정답을 찾을 수 있습니다.



N_str = input()
N = int(N_str)

# 1. 자릿수 계산 (문자열 길이를 이용)
digit_count = len(N_str)

# 2. 탐색 시작 지점 계산: N - (자릿수 * 9)
# 단, 시작 지점이 음수가 되면 안 되므로 max(0, ...)를 사용합니다.
start = max(0, N - (digit_count * 9))

result = 0
# 3. 전체 범위(1~N)가 아니라 최적화된 범위(start~N)만 탐색
for i in range(start, N):
    digit_sum = sum(map(int, str(i)))
    if N == i + digit_sum:
        result = i
        break

print(result)



